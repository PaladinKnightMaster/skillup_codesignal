# Create a numeric vector
monthly_revenue <- c(1200, 1500, 1300, 1600, 1700)
print("Monthly Revenue:")
print(monthly_revenue)

# Calculate the mean
mean_revenue <- mean(monthly_revenue)
print("Mean Revenue:")
print(mean_revenue)

# Calculate the minimum value
min_revenue <- min(monthly_revenue)
print("Minimum Revenue:")
print(min_revenue)

# Calculate the maximum value
max_revenue <- max(monthly_revenue)
print("Maximum Revenue:")
print(max_revenue)

# Calculate the sum
sum_revenue <- sum(monthly_revenue)
print("Sum of Revenue:")
print(sum_revenue)