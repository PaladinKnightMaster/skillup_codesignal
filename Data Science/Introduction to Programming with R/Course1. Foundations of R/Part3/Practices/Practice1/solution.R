# Assigning numeric variables
a <- 7
b <- 42

# Using comparison operators
is_equal <- a == b
is_not_equal <- a != b
is_greater <- a > b
is_less <- a < b
is_greater_equal <- a >= b
is_less_equal <- a <= b

# Print the results
print(is_equal)
print(is_not_equal)
print(is_greater)
print(is_less)
print(is_greater_equal)
print(is_less_equal)